#Форматирование строки

'''
==================================
|    |    1||    2||    3||    4||
==================================
'''
n = int(input('Введите любое число меньше 100: '))
if n:
    n = n % 100
    print('=' * (10 * n))
    print('|     ||', end='')
    for i in range(1, n + 1):
        if n > 9:
            print('  %d ||' % (i), end='')
        else:
            print('    %d ||' % (i), end='')
    print(' ')
    print('=' * (10 * n))
else:
    print('введите число: ')